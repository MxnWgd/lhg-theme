<h1>Beschlusssammlung</h1>
<img src="<?php echo get_template_directory_uri(); ?>/inc/help/img/resolutions1.png" alt="Musterbeschluss" style="width: 100%; max-width: 600px;">
<p>Über den Menüpunkt <strong>Beschlüsse</strong> kann eine vollständige Beschlusssammlung angelegt werden, welche dann auf der Website angezeigt werden kann. Dabei können Websitebesucher die Beschlüsse nach verschiedenen Attributen filtern oder gezielt nach bestimmten Beschlüssen suchen.</p>


<h3>Erstellen von Beschlüssen</h3>
<p>Über das Menüelement Beschlüsse links können vorhandene Beschlüsse bearbeitet und neue Beschlüsse erstellt werden.</p>
<p>Ein Beschluss sollte mindestens über einen Titel, einen Antragstext in der Beschlussfassung, einen Antragssteller sowie die Versammlung, auf der dieser beschlossen wurde, umfassen.</p>
<p>Zusätzlich können Beschlüsse mit Schlagworten versehen werden, um Benutzern eine leichtere inhaltliche Filterung zu ermöglichen.</p>


<h3>Verwaltung der Attributen</h3>
<div class="notice-info notice">
    <p>Beim Anlegen neuer Beschlüsse können neue Antragssteller, Versammlungen und Schlagworte direkt erstellt werden - sie müssen <em>nicht</em> zuerst über die Attributverwaltung angelegt werden.</p>
</div>
<p>Im Beschluss-Menü links können die Attribute der Beschlüsse separat verwaltet werden.</p>
<ul style="padding-left: 20px;">
  <li>
    <h3>Antragssteller</h3>
    <p>Hier können die antragsstellenden Personen und Gremien verwaltet werden.</p>
  </li>
  <li>
    <h3>Versammlungen</h3>
    <p>Versammlungen (z.B. Mitgliederversammlungen), auf denen Beschlüsse gefasst wurden, können hier verwaltet werden.</p>
    <p>Es empfiehlt sich, zur besseren Übersichtlichkeit diese ggf. mit einem Datum (bspw. <em>"Mitgliederversammlung 01.02.2003"</em>) zu versehen.</p>
    <p>Versammlungen lassen sich zudem hierarchisch strukturieren, so können bspw. unter einer Versammlungkategorie "Mitgliederversammlung" alle entsprechenden Versammlungen gruppiert werden.</p>
  </li>
  <li>
    <h3>Schlagworte</h3>
    <p>Schlagworte (bspw. <em>"Innerverbandliches"</em>, <em>"Satzungsänderungen"</em>, <em>"Freiheit"</em>, etc.) dienen der schnelleren inhaltlichen Einordnung von Anträgen.</p>
    <p>Insbesondere bei besonders umfangreichen Beschlusssammlungen empfiehlt es sich, diese sinnvoll einzusetzen.</p>
  </li>
</ul>


<h3>Beschlusssammlung anzeigen</h3>
<p>Für die Beschlusssammlung wird die Inhaltstyp-Archiv-Funktion der Beschlüsse verwendet. Dieses muss also an entsprechenden Stellen verlinkt werden.</p>

<h4>Einfügen in ein Menü</h4>
<img src="<?php echo get_template_directory_uri(); ?>/inc/help/img/resolutions2.png" alt="Menüansicht zum Hinzufügen der Beschlusssammlung" style="width: 300px; float: right;">
<div class="notice-info notice">
	<p>Ist der Abschnitt <em>Beschlüsse</em> in der Menüverwaltung nicht sichtbar, sollte über <em>Ansicht anpassen</em> oben rechts sichergestellt werden, dass der Haken bei <em>Beschlüsse</em> gesetzt ist.</p>
</div>
<p>Um die Beschlusssammlung von einem Menü aus zu erreichen, muss der Menüeintrag <em>Alle Beschlüsse</em> hinzugefügt werden.</p>
<p>Hierzu unter <em>Menüeinträge hinzufügen</em> den Abschnitt <em>Beschlüsse</em> aufklappen, dort den Reiter <em>Alle</em> auswählen und den Eintrag <em>Alle Beschlüsse</em> auswählen.</p>
